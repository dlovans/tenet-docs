<script>
  import { cva } from "class-variance-authority";
  import { cn } from "$lib/utils";

  let {
    class: className,
    variant = "default",
    size = "default",
    children,
    onclick,
    href = undefined,
    ...rest
  } = $props();

  const buttonVariants = cva(
    "inline-flex items-center justify-center gap-1.5 whitespace-nowrap text-sm font-medium transition-all focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-ring focus-visible:ring-offset-2 disabled:pointer-events-none disabled:opacity-50",
    {
      variants: {
        variant: {
          default:
            "bg-primary text-primary-foreground hover:bg-primary/90 rounded-lg",
          destructive:
            "bg-destructive text-destructive-foreground hover:bg-destructive/90 rounded-lg",
          outline:
            "border border-input bg-background hover:bg-accent hover:text-accent-foreground rounded-lg",
          secondary:
            "bg-secondary text-secondary-foreground hover:bg-secondary/80 rounded-lg",
          ghost: "hover:bg-accent hover:text-accent-foreground rounded-lg",
          link: "text-primary underline-offset-4 hover:underline",
        },
        size: {
          default: "h-9 px-4 py-2",
          sm: "h-8 px-3",
          lg: "h-10 px-6",
          icon: "h-9 w-9",
        },
      },
      defaultVariants: {
        variant: "default",
        size: "default",
      },
    },
  );
</script>

{#if href}
  <a {href} class={cn(buttonVariants({ variant, size, className }))} {...rest}>
    {@render children?.()}
  </a>
{:else}
  <button
    class={cn(buttonVariants({ variant, size, className }))}
    {onclick}
    {...rest}
  >
    {@render children?.()}
  </button>
{/if}
